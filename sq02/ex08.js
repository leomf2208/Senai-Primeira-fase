let numeroA
let numeroB

numeroA = Number(prompt("Digite o numero A "))
numeroB = Number(prompt("Digite o numero B "))

if(numeroA > numeroB){
    alert("numeroA é maior")
}
else if(numeroA < numeroB){
    alert("numeroB é maior")
}
else if(numeroA = numeroB){
    alert("numeroA é igual a numeroB")
}
else{
    alert("invalido")
}
